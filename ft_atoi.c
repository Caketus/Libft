int		ft_atoi(const char *str)
{
	int		x;
	int		symb;
	int		nb;

	x = 0;
	symb = 1;
	nb = 0;
	while (str[x] == '\t' || str[x] == '\v' || str[x] == '\n'
			|| str[x] == '\r' || str[x] == '\f' || str[x] == ' ')
		x++;
	if (str[x] == '+' || str[x] == '-')
	{
		if (str[x] == '-')
			symb = symb * (-1);
		x++;
	}
	while (str[x] >= '0' && str[x] <= '9')
	{
		nb = (nb * 10) + str[x] - 48;
		x++;
	}
	return (str[x] == ('-' | '+') ? (0) : (nb * symb));
}
